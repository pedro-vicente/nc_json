#ifdef HAVE_NETCDF
#include <netcdf.h>
#endif
#include "nc_api.hh"

/////////////////////////////////////////////////////////////////////////////////////////////////////
//cf_create
/////////////////////////////////////////////////////////////////////////////////////////////////////

int cf_create(const char *path, int *ncidp)
{
#ifdef HAVE_NETCDF
  if (nc_create(path, NC_WRITE, ncidp) != NC_NOERR)
  {
    return -1;
  }
#endif
  return 0;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////
//cf_close
/////////////////////////////////////////////////////////////////////////////////////////////////////

int cf_close(int ncid)
{
#ifdef HAVE_NETCDF
  if (nc_close(ncid) != NC_NOERR)
  {
    return -1;
  }
#endif
  return 0;
}
